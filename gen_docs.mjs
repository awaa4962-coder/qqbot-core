function secTitle(text) {
  return new Paragraph({
    spacing: { before: 300, after: 150 },
    children: [new TextRun({ text, bold: true, size: 30, font: 'SimHei' })],
  });
}

function body(text) {
  return new Paragraph({
    spacing: { after: 200, line: 360 },
    children: [new TextRun({ text, size: 28, font: 'SimSun' })],
  });
}

